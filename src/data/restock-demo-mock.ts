export const RESTOCK_DEMO_KEY = "restock_demo_mode";

export const isRestockDemoActive = () => {
  if (typeof window === "undefined") return false;
  return window.localStorage.getItem(RESTOCK_DEMO_KEY) === "1";
};

export const setRestockDemo = (on: boolean) => {
  if (typeof window === "undefined") return;
  if (on) window.localStorage.setItem(RESTOCK_DEMO_KEY, "1");
  else window.localStorage.removeItem(RESTOCK_DEMO_KEY);
  window.dispatchEvent(new Event("restock-demo-changed"));
};

