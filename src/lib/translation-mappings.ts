export function autoTranslate(name: string, locale: "fr" | "nl" | "de"): string | null {
  const map = locale === "fr" ? EN_TO_FR : locale === "nl" ? EN_TO_NL : EN_TO_DE;
  // Exact match
  if (map[name]) return map[name];
  // Case-insensitive
  const lower = name.toLowerCase();
  for (const [en, loc] of Object.entries(map)) {
    if (en.toLowerCase() === lower) return loc;
  }
  // Partial match (contains)
  for (const [en, loc] of Object.entries(map)) {
    if (lower.includes(en.toLowerCase())) return name.replace(new RegExp(en, "i"), loc);
  }
  return null;
}
